N = int(input())

dp = [0 for _ in range(N+1)]

# dp 1 = 1 | [1]
# dp 2 = 2 | [1 1], [2]
# dp 3 = 3 | [1 1 1], [1 2], [2 1]
# dp 4 = 5 | [1 1 1 1], [1 1 2], [1 2 1], [2 1 1], [2 2]
# dp 5 = 8 | [1 1 1 1 1], [1 1 1 2], [1 1 2 1], [1 2 1 1], [2 1 1 1], [1 2 2], [2 1 2], [2 2 1]

dp[0] = 0
dp[1] = 1
dp[2] = 2

for i in range(3,N+1):
    dp[i] = dp[i-1] + dp[i-2]

print(dp[N]%10007)
        
